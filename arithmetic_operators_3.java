public class arithmetic_operators_3 {
    public static void main(String[] args){
        int a=60, b=15, c=13, result;

        result = a+b;
        System.out.println("\n"+result);

        result = a-b;
        System.out.println('\n'+result);

        result = a*b;
        System.out.println("\n"+result);

        result = a/c;
        System.out.println("\n"+result);
        
        result = a%c;
        System.out.println("\n"+result);
        
        a++;
        System.out.println("\n increment a by 1: "+ a);

        b--;
        System.out.println("\n decrement b by 1: "+ b);


    }
}
